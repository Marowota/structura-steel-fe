export const colorToAlpha = (color: string, alpha: number): string => {
  const validAlpha = Math.max(0, Math.min(1, alpha));

  return color.replace(
    /rgba\((\d+),\s*(\d+),\s*(\d+),\s*[\d.]+\)/,
    (_, r, g, b) => `rgba(${r}, ${g}, ${b}, ${validAlpha})`,
  );
};
